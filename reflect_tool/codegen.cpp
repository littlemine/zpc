  static std::string escape_string(std::string_view s) {
    std::string out;
    out.reserve(s.size());
    for (char c : s) {
      if (c == '"')  out += "\\\"";
      else if (c == '\\') out += "\\\\";
      else if (c == '\n') out += "\\n";
      else out += c;
    }
    return out;
  }

  static std::string access_enum(std::string_view s) {
    if (s == "public")    return "::zs::reflect::Access::Public";
    if (s == "protected") return "::zs::reflect::Access::Protected";
    if (s == "private")   return "::zs::reflect::Access::Private";
    return "::zs::reflect::Access::None";
  }

  /// Convert a MetadataContainer property value to a C string literal.
  static std::string meta_value_literal(const MetaValue& v) {
    std::string valStr;
    std::visit([&](auto&& arg) {
      using T = std::decay_t<decltype(arg)>;
      if constexpr (std::is_same_v<T, std::string>)
        valStr = fmt::format("\"{}\"", escape_string(arg));
      else if constexpr (std::is_same_v<T, int>)
        valStr = fmt::format("\"{}\"", arg);
      else if constexpr (std::is_same_v<T, float>)
        valStr = fmt::format("\"{}\"", arg);
      else if constexpr (std::is_same_v<T, std::vector<std::string>>) {
        valStr = "\"";
        for (size_t i = 0; i < arg.size(); ++i) {
          if (i) valStr += ",";
          valStr += escape_string(arg[i]);
        }
        valStr += "\"";
      } else if constexpr (std::is_same_v<T, std::vector<float>>) {
        valStr = "\"";
        for (size_t i = 0; i < arg.size(); ++i) {
          if (i) valStr += ",";
          valStr += fmt::format("{}", arg[i]);
        }
        valStr += "\"";
      } else {
        valStr = "\"\"";
      }
    }, v);
    return valStr;
  }

  /// Emit a static MetaEntry array for the given metadata container.
  static void emit_meta_array(std::ostringstream& os,
                               const std::string& arrayName,
                               const MetadataContainer& meta) {
    os << fmt::format("static const ::zs::reflect::MetaEntry {}[] = {{\n",
                      arrayName);
    for (auto& [k, v] : meta.properties) {
      os << fmt::format("  {{R\"({})\", {}}},\n", k, meta_value_literal(v));
    }
    os << "};\n";
  }

  // -----------------------------------------------------------------------
  // emit_reflected_header — per-source generated header
  // -----------------------------------------------------------------------

  void emit_reflected_header(const std::string& outputPath,
                             const std::vector<CollectedType>& types,
                             bool verbose) {
    std::ostringstream os;

    os << "// ============================================================\n"
       << "// Auto-generated by zpc_reflect_tool — DO NOT EDIT\n"
       << "// ============================================================\n"
       << "#pragma once\n"
       << "#include <zensim/reflect/ZpcReflectRuntime.hpp>\n"
       << "#include <zensim/reflect/ZpcReflectRegistry.hpp>\n"
       << "#include <zensim/reflect/ZpcReflectObject.hpp>\n\n";

    // Forward declarations
    for (auto& ct : types) {
      if (!ct.forwardDecl.empty()) os << ct.forwardDecl;
    }
    os << "\n";

    // TypeInfoOf specializations
    for (auto& ct : types) {
      const std::string varName = convert_to_valid_cpp_var_name(ct.qualifiedName);

      // ================================================================
      // Phase 1: emit static arrays that will be referenced by TypeInfo
      // ================================================================

      // --- Per-field metadata arrays ---
      for (size_t fi = 0; fi < ct.fields.size(); ++fi) {
        auto& f = ct.fields[fi];
        if (f.metadata.properties.empty()) continue;
        emit_meta_array(os, fmt::format("_zs_fmeta_{}_{}", varName, f.name),
                        f.metadata);
      }

      // --- Per-method param arrays and metadata arrays ---
      {
        std::unordered_map<std::string, int> nameCount;
        for (auto& m : ct.methods) {
          int idx = nameCount[m.name]++;
          std::string mSuffix = idx > 0 ? fmt::format("_{}", idx) : "";

          if (!m.params.empty()) {
            os << fmt::format(
                "static const ::zs::reflect::ParamInfo _zs_mparams_{}_{}{}[] = {{\n",
                varName, m.name, mSuffix);
            for (auto& p : m.params) {
              os << fmt::format("  {{R\"({})\", R\"({})\", {}, {}}},\n",
                                p.name, p.typeName,
                                p.hasDefault ? "true" : "false",
                                p.hasDefault ? fmt::format("R\"({})\"", escape_string(p.defaultExpr)) : "nullptr");
            }
            os << "};\n";
          }
          if (!m.metadata.properties.empty()) {
            emit_meta_array(os,
                fmt::format("_zs_mmeta_{}_{}{}", varName, m.name, mSuffix),
                m.metadata);
          }
        }
      }

      // --- FieldInfo array ---
      if (!ct.fields.empty()) {
        os << fmt::format(
            "static const ::zs::reflect::FieldInfo _zs_ti_fields_{}[] = {{\n",
            varName);
        for (auto& f : ct.fields) {
          std::string typeIdStr = "::zs::reflect::TypeId::Unknown";
          if (f.typeName == "bool" || f.typeName == "_Bool")
            typeIdStr = "::zs::reflect::TypeId::Bool";
          else if (f.typeName == "unsigned char" || f.typeName == "uint8_t")
            typeIdStr = "::zs::reflect::TypeId::Uint8";
          else if (f.typeName == "char" || f.typeName == "signed char" || f.typeName == "int8_t")
            typeIdStr = "::zs::reflect::TypeId::Int8";
          else if (f.typeName == "short" || f.typeName == "signed short" || f.typeName == "int16_t")
            typeIdStr = "::zs::reflect::TypeId::Int16";
          else if (f.typeName == "unsigned short" || f.typeName == "uint16_t")
            typeIdStr = "::zs::reflect::TypeId::Uint16";
          else if (f.typeName == "int" || f.typeName == "signed int" || f.typeName == "int32_t")
            typeIdStr = "::zs::reflect::TypeId::Int32";
          else if (f.typeName == "unsigned int" || f.typeName == "uint32_t")
            typeIdStr = "::zs::reflect::TypeId::Uint32";
          else if (f.typeName == "long long" || f.typeName == "signed long long" || f.typeName == "int64_t")
            typeIdStr = "::zs::reflect::TypeId::Int64";
          else if (f.typeName == "unsigned long long" || f.typeName == "uint64_t")
            typeIdStr = "::zs::reflect::TypeId::Uint64";
          else if (f.typeName == "float")
            typeIdStr = "::zs::reflect::TypeId::Float";
          else if (f.typeName == "double")
            typeIdStr = "::zs::reflect::TypeId::Double";
          else if (f.typeName.find("string") != std::string::npos)
            typeIdStr = "::zs::reflect::TypeId::String";
          else if (f.isPointer) typeIdStr = "::zs::reflect::TypeId::Pointer";
          else if (f.isReference) typeIdStr = "::zs::reflect::TypeId::Reference";
          else typeIdStr = "::zs::reflect::TypeId::Record";

          bool isTransient = false;
          bool isSerializable = true;
          for (auto& [mk, mv] : f.metadata.properties) {
            if (mk == "transient") {
              if (auto* s = std::get_if<std::string>(&mv); s && *s == "true")
                isTransient = true;
            }
            if (mk == "serializable") {
              if (auto* s = std::get_if<std::string>(&mv); s && *s == "false")
                isSerializable = false;
            }
          }

          std::string metaRef = "nullptr";
          std::string metaCnt = "0";
          if (!f.metadata.properties.empty()) {
            metaRef = fmt::format("_zs_fmeta_{}_{}", varName, f.name);
            metaCnt = fmt::format("{}", f.metadata.properties.size());
          }

          os << fmt::format(
              "  {{R\"({})\", R\"({})\", 0x{:016X}ULL, {}, {},"
              " {}, {}, {}, {}, {}, {}, {}, {}, {}, {}}},\n",
              f.name, f.typeNameNoTag, f.typeHash, typeIdStr,
              access_enum(f.accessStr),
              f.offset, f.size,
              f.isPointer ? "true" : "false",
              f.isReference ? "true" : "false",
              f.isConst ? "true" : "false",
              f.isStatic ? "true" : "false",
              isTransient ? "true" : "false",
              isSerializable ? "true" : "false",
              metaRef, metaCnt);
        }
        os << "};\n";
      }

      // --- MethodInfo array ---
      if (!ct.methods.empty()) {
        os << fmt::format(
            "static const ::zs::reflect::MethodInfo _zs_ti_methods_{}[] = {{\n",
            varName);
        std::unordered_map<std::string, int> nameCount2;
        for (auto& m : ct.methods) {
          int idx = nameCount2[m.name]++;
          std::string mSuffix = idx > 0 ? fmt::format("_{}", idx) : "";

          std::string paramsRef = "nullptr";
          std::string paramsCnt = "0";
          if (!m.params.empty()) {
            paramsRef = fmt::format("_zs_mparams_{}_{}{}", varName, m.name, mSuffix);
            paramsCnt = fmt::format("{}", m.params.size());
          }

          std::string metaRef = "nullptr";
          std::string metaCnt = "0";
          if (!m.metadata.properties.empty()) {
            metaRef = fmt::format("_zs_mmeta_{}_{}{}", varName, m.name, mSuffix);
            metaCnt = fmt::format("{}", m.metadata.properties.size());
          }

          os << fmt::format(
              "  {{R\"({})\", R\"({})\", {}, {}, {}, {}, {}, {}, {}, {}, {}, {}}},\n",
              m.name, m.returnType,
              paramsRef, paramsCnt,
              access_enum(m.accessStr),
              m.isStatic ? "true" : "false",
              m.isConst ? "true" : "false",
              m.isVirtual ? "true" : "false",
              m.isPureVirtual ? "true" : "false",
              m.isNoexcept ? "true" : "false",
              metaRef, metaCnt);
        }
        os << "};\n";
      }

      // --- BaseInfo array ---
      if (!ct.bases.empty()) {
        os << fmt::format(
            "static const ::zs::reflect::BaseInfo _zs_ti_bases_{}[] = {{\n",
            varName);
        for (auto& b : ct.bases) {
          os << fmt::format("  {{R\"({})\", 0x{:016X}ULL, {}, {}}},\n",
                            b.nameNoTag, b.typeHash, access_enum(b.accessStr),
                            b.isVirtual ? "true" : "false");
        }
        os << "};\n";
      }

      // --- EnumValueInfo array ---
      if (!ct.enumValues.empty()) {
        // Per-enum-value metadata
        for (size_t evi = 0; evi < ct.enumValues.size(); ++evi) {
          auto& ev = ct.enumValues[evi];
          if (ev.metadata.properties.empty()) continue;
          emit_meta_array(os, fmt::format("_zs_evmeta_{}_{}", varName, ev.name),
                          ev.metadata);
        }

        os << fmt::format(
            "static const ::zs::reflect::EnumValueInfo _zs_ti_enums_{}[] = {{\n",
            varName);
        for (auto& ev : ct.enumValues) {
          std::string metaRef = "nullptr";
          std::string metaCnt = "0";
          if (!ev.metadata.properties.empty()) {
            metaRef = fmt::format("_zs_evmeta_{}_{}", varName, ev.name);
            metaCnt = fmt::format("{}", ev.metadata.properties.size());
          }
          os << fmt::format("  {{R\"({})\", {}LL, {}, {}}},\n",
                            ev.name, ev.value, metaRef, metaCnt);
        }
        os << "};\n";
      }

      // --- Type-level metadata ---
      if (!ct.metadata.properties.empty()) {
        emit_meta_array(os, fmt::format("_zs_ti_meta_{}", varName),
                        ct.metadata);
      }

      os << "\n";

      // ================================================================
      // Phase 2: emit TypeInfoOf specialization
      // ================================================================

      os << "// ---- " << ct.qualifiedName << " ----\n";
      os << "template <>\n";
      os << fmt::format("struct ::zs::reflect::TypeInfoOf<{}> {{\n", ct.nameNoTag);
      os << "  static constexpr bool reflected = true;\n\n";
      os << "  static const ::zs::reflect::TypeInfo& get() {\n";
      os << "    static const ::zs::reflect::TypeInfo info = {\n";
      os << fmt::format("      /* name        */ R\"({})\",\n", ct.qualifiedName);
      os << fmt::format("      /* displayName */ R\"({})\",\n", ct.displayName);
      os << fmt::format("      /* typeHash    */ 0x{:016X}ULL,\n", ct.typeHash);
      os << fmt::format("      /* size        */ {},\n", ct.size);
      os << fmt::format("      /* align       */ {},\n", ct.align);
      os << fmt::format("      /* isClass     */ {},\n", ct.isClass ? "true" : "false");
      os << fmt::format("      /* isStruct    */ {},\n", ct.isStruct ? "true" : "false");
      os << fmt::format("      /* isUnion     */ {},\n", ct.isUnion ? "true" : "false");
      os << fmt::format("      /* isEnum      */ {},\n", ct.isEnum ? "true" : "false");
      os << fmt::format("      /* isAbstract  */ {},\n", ct.isAbstract ? "true" : "false");
      os << fmt::format("      /* isAggregate */ {},\n", ct.isAggregate ? "true" : "false");

      // fields pointer + count
      os << fmt::format("      /* fields      */ {}, {},\n",
          ct.fields.empty() ? "nullptr" : fmt::format("_zs_ti_fields_{}", varName),
          ct.fields.size());

      // methods pointer + count
      os << fmt::format("      /* methods     */ {}, {},\n",
          ct.methods.empty() ? "nullptr" : fmt::format("_zs_ti_methods_{}", varName),
          ct.methods.size());

      // bases pointer + count
      os << fmt::format("      /* bases       */ {}, {},\n",
          ct.bases.empty() ? "nullptr" : fmt::format("_zs_ti_bases_{}", varName),
          ct.bases.size());

      // enumValues pointer + count
      os << fmt::format("      /* enumValues  */ {}, {},\n",
          ct.enumValues.empty() ? "nullptr" : fmt::format("_zs_ti_enums_{}", varName),
          ct.enumValues.size());

      // type metadata pointer + count
      os << fmt::format("      /* metadata    */ {}, {}\n",
          ct.metadata.properties.empty() ? "nullptr" : fmt::format("_zs_ti_meta_{}", varName),
          ct.metadata.properties.size());

      os << "    };\n";
      os << "    return info;\n";
      os << "  }\n";
      os << "};\n\n";

      // Static auto-registration
      os << fmt::format(
          "static const ::zs::reflect::AutoRegister _zs_autoreg_{}"
          "(& ::zs::reflect::TypeInfoOf<{}>::get());\n\n",
          varName, ct.nameNoTag);

      // ---- AccessorOf specialization (static function pointers + flat arrays) ----
      if (!ct.isEnum) {
        // Count accessible fields and methods
        int accessibleFieldCount = 0;
        for (auto& f : ct.fields) {
          if (!f.isStatic) ++accessibleFieldCount;
        }
        int accessibleMethodCount = 0;
        for (auto& m : ct.methods) {
          if (m.accessStr == "public" && !m.isPureVirtual) ++accessibleMethodCount;
        }

        // ---- Static getter functions ----
        for (auto& f : ct.fields) {
          if (f.isStatic) continue;
          os << fmt::format(
              "static void _zs_get_{0}_{1}(const void* obj, ::zs::reflect::Any* out) {{\n"
              "  *out = ::zs::reflect::Any(static_cast<const {2}*>(obj)->{1});\n"
              "}}\n",
              varName, f.name, ct.nameNoTag);
        }

        // ---- Static setter functions (skip const fields) ----
        for (auto& f : ct.fields) {
          if (f.isStatic || f.isConst) continue;
          os << fmt::format(
              "static void _zs_set_{0}_{1}(void* obj, const ::zs::reflect::Any* val) {{\n"
              "  static_cast<{2}*>(obj)->{1} = val->as<{3}>();\n"
              "}}\n",
              varName, f.name, ct.nameNoTag, f.typeNameNoTag);
        }

        // ---- Static method invoker functions ----
        struct MethodFuncEntry { std::string name; std::string funcName; };
        std::vector<MethodFuncEntry> methodEntries;
        {
          std::unordered_map<std::string, int> overloadIdx;
          for (auto& m : ct.methods) {
            if (m.accessStr != "public") continue;
            if (m.isPureVirtual) continue;

            int idx = overloadIdx[m.name]++;
            std::string suffix = idx > 0 ? fmt::format("_{}", idx) : "";
            std::string funcName = fmt::format("_zs_invoke_{0}_{1}{2}", varName, m.name, suffix);

            bool isVoidReturn = (m.returnType == "void");

            // Build the call expression
            std::string callExpr;
            if (m.isStatic) {
              callExpr = fmt::format("{}::{}", ct.nameNoTag, m.name);
            } else if (m.isConst) {
              callExpr = fmt::format(
                  "static_cast<const {}*>(obj)->{}", ct.nameNoTag, m.name);
            } else {
              callExpr = fmt::format(
                  "static_cast<{}*>(obj)->{}", ct.nameNoTag, m.name);
            }

            // Build parameter list
            std::string paramList;
            for (size_t pi = 0; pi < m.params.size(); ++pi) {
              if (pi > 0) paramList += ", ";
              paramList += fmt::format("args[{}].as<{}>()", pi, m.params[pi].typeName);
            }

            os << fmt::format(
                "static void {}(void* obj, const ::zs::reflect::Any* args,"
                " int nargs, ::zs::reflect::Any* ret) {{\n",
                funcName);

            if (isVoidReturn) {
              os << fmt::format("  {}({});\n"
                                "  (void)ret; (void)args; (void)nargs;\n",
                                callExpr, paramList);
            } else {
              os << fmt::format("  auto _r = {}({});\n"
                                "  if (ret) *ret = ::zs::reflect::Any(_r);\n"
                                "  (void)args; (void)nargs;\n",
                                callExpr, paramList);
            }
            os << "}\n";

            methodEntries.push_back({m.name, funcName});
          }
        }

        os << "\n";

        // ---- Static field accessor array ----
        if (accessibleFieldCount > 0) {
          os << fmt::format(
              "static const ::zs::reflect::FieldAccessor _zs_fields_{}[] = {{\n",
              varName);
          for (auto& f : ct.fields) {
            if (f.isStatic) continue;
            std::string setFn = f.isConst
                ? "nullptr"
                : fmt::format("&_zs_set_{}_{}", varName, f.name);
            os << fmt::format("  {{\"{}\", &_zs_get_{}_{}, {}}},\n",
                              f.name, varName, f.name, setFn);
          }
          os << "};\n\n";
        }

        // ---- Static method accessor array ----
        if (accessibleMethodCount > 0) {
          os << fmt::format(
              "static const ::zs::reflect::MethodAccessor _zs_methods_{}[] = {{\n",
              varName);
          for (auto& me : methodEntries) {
            os << fmt::format("  {{\"{}\", &{}}},\n", me.name, me.funcName);
          }
          os << "};\n\n";
        }

        // ---- AccessorOf template specialization ----
        os << fmt::format(
            "template <>\n"
            "struct ::zs::reflect::AccessorOf<{}> {{\n"
            "  static constexpr bool available = true;\n\n"
            "  static const ::zs::reflect::Accessor& get() {{\n"
            "    static const ::zs::reflect::Accessor acc = {{\n"
            "      &::zs::reflect::TypeInfoOf<{}>::get(),\n"
            "      {}, {},\n"
            "      {}, {}\n"
            "    }};\n"
            "    return acc;\n"
            "  }}\n"
            "}};\n\n",
            ct.nameNoTag, ct.nameNoTag,
            accessibleFieldCount > 0
                ? fmt::format("_zs_fields_{}", varName) : "nullptr",
            accessibleFieldCount,
            accessibleMethodCount > 0
                ? fmt::format("_zs_methods_{}", varName) : "nullptr",
            accessibleMethodCount);

        // Auto-register the accessor
        os << fmt::format(
            "static const ::zs::reflect::AutoRegisterAccessor _zs_accreg_{}"
            "(0x{:016X}ULL, &::zs::reflect::AccessorOf<{}>::get());\n\n",
            varName, ct.typeHash, ct.nameNoTag);
      }
    }

    // Write to file
    std::ofstream ofs(outputPath, std::ios::out | std::ios::trunc);
    if (!ofs) {
      std::cerr << fmt::format("Cannot write generated header: {}\n", outputPath);
      return;
    }
    ofs << os.str();
    if (verbose)
      fmt::print("  Wrote {} type(s) → {}\n", types.size(), outputPath);
  }

  // -----------------------------------------------------------------------
  // emit_register_source — aggregated registration .cpp
  // -----------------------------------------------------------------------

  void emit_register_source(const std::string& outputPath,
                            const ReflectionModel& model,
                            const CodeCompilerState& state,
                            const std::string& targetName,
                            bool verbose) {
    std::ostringstream os;

    os << "// ============================================================\n"
       << "// Auto-generated by zpc_reflect_tool — DO NOT EDIT\n"
       << "// Target: " << targetName << "\n"
       << "// ============================================================\n"
       << "#include <zensim/reflect/ZpcReflectRegistry.hpp>\n\n";

    // Include original source files so reflected types are complete definitions
    for (auto& src : model.inputSourcePaths) {
      os << fmt::format("#include \"{}\"\n", src);
    }
    os << "\n";

    // Include all generated per-source headers
    for (auto& h : model.generatedHeaders) {
      os << fmt::format("#include \"{}\"\n", h);
    }
    os << "\n";

    // A registration function that can be called explicitly if needed
    os << fmt::format(
        "namespace zs::reflect::generated_{} {{\n"
        "  void register_all_types() {{\n"
        "    // Types are auto-registered via static initializers in\n"
        "    // the included generated headers.  This function exists\n"
        "    // so that users can force the linker to pull in the TU.\n"
        "  }}\n"
        "}} // namespace zs::reflect::generated_{}\n",
        targetName, targetName);

    // Ensure output directory exists
    auto parentDir = std::filesystem::path(outputPath).parent_path();
    if (!parentDir.empty()) mkdirs(parentDir.string());

    std::ofstream ofs(outputPath, std::ios::out | std::ios::trunc);
    if (!ofs) {
      std::cerr << fmt::format("Cannot write registration source: {}\n", outputPath);
      return;
    }
    ofs << os.str();
    if (verbose)
      fmt::print("  Wrote registration source → {}\n", outputPath);
  }

