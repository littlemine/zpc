  /// Map a C++ type name to a string for the ctypes return/param type.
  /// Returns the C type we will use in the extern "C" function signature.
  static std::string cpp_to_c_type(const std::string& typeName) {
    // Normalize: strip const, &, *, whitespace for matching
    std::string t = typeName;
    // Remove leading/trailing whitespace
    while (!t.empty() && t.front() == ' ') t.erase(t.begin());
    while (!t.empty() && t.back() == ' ')  t.pop_back();

    if (t == "void")      return "void";
    if (t == "bool")      return "bool";
    if (t == "int8_t"  || t == "char" || t == "signed char") return "int8_t";
    if (t == "int16_t" || t == "short")    return "int16_t";
    if (t == "int32_t" || t == "int")      return "int32_t";
    if (t == "int64_t" || t == "long long") return "int64_t";
    // NOTE: C++ 'long' is 4 bytes on Windows (MSVC/LLP64) but 8 bytes on
    // most Unix (LP64).  Since the reflect tool uses Clang (which follows
    // the host ABI), we map 'long' based on the platform that will load
    // the generated bindings.  Using int32_t on Windows is the safe default.
#ifdef _MSC_VER
    if (t == "long") return "int32_t";
#else
    if (t == "long") return "int64_t";
#endif
    if (t == "uint8_t" || t == "unsigned char") return "uint8_t";
    if (t == "uint16_t"|| t == "unsigned short") return "uint16_t";
    if (t == "uint32_t"|| t == "unsigned int" || t == "unsigned") return "uint32_t";
    if (t == "uint64_t"|| t == "unsigned long long") return "uint64_t";
#ifdef _MSC_VER
    if (t == "unsigned long") return "uint32_t";
#else
    if (t == "unsigned long") return "uint64_t";
#endif
    if (t == "float")     return "float";
    if (t == "double")    return "double";
    if (t.find("string") != std::string::npos) return "const char*";
    return ""; // empty → not a simple C type, use void* / skip
  }

  /// Map C type to Python ctypes name.
  static std::string c_type_to_ctypes(const std::string& ctype) {
    if (ctype == "void")        return "None";
    if (ctype == "bool")        return "ctypes.c_bool";
    if (ctype == "int8_t")      return "ctypes.c_int8";
    if (ctype == "int16_t")     return "ctypes.c_int16";
    if (ctype == "int32_t")     return "ctypes.c_int32";
    if (ctype == "int64_t")     return "ctypes.c_int64";
    if (ctype == "uint8_t")     return "ctypes.c_uint8";
    if (ctype == "uint16_t")    return "ctypes.c_uint16";
    if (ctype == "uint32_t")    return "ctypes.c_uint32";
    if (ctype == "uint64_t")    return "ctypes.c_uint64";
    if (ctype == "float")       return "ctypes.c_float";
    if (ctype == "double")      return "ctypes.c_double";
    if (ctype == "const char*") return "ctypes.c_char_p";
    if (ctype == "void*")       return "ctypes.c_void_p";
    return "ctypes.c_void_p";
  }

  /// Return true if the C++ field type can be directly mapped to a C type.
  static bool is_simple_c_type(const std::string& typeName) {
    return !cpp_to_c_type(typeName).empty();
  }

  /// Sanitize a C++ qualified name to be safe for C function names.
  static std::string sanitize_for_c(const std::string& name) {
    std::string out;
    for (char c : name) {
      if (c == ':' || c == '<' || c == '>' || c == ',' || c == ' ')
        out += '_';
      else
        out += c;
    }
    // collapse consecutive underscores
    std::string collapsed;
    for (char c : out) {
      if (c == '_' && !collapsed.empty() && collapsed.back() == '_') continue;
      collapsed += c;
    }
    return collapsed;
  }

  // -----------------------------------------------------------------------
  // emit_python_bindings — per-target C-ABI source
  // -----------------------------------------------------------------------

  void emit_python_bindings(const std::string& outputPath,
                            const std::vector<CollectedType>& allTypes,
                            const std::string& targetName,
                            bool verbose) {
    std::ostringstream os;

    os << "// ============================================================\n"
       << "// Auto-generated by zpc_reflect_tool — DO NOT EDIT\n"
       << "// Python ctypes C-ABI bindings for target: " << targetName << "\n"
       << "// ============================================================\n"
       << "#include <zensim/reflect/ZpcReflectPyExport.hpp>\n"
       << "#include <zensim/reflect/ZpcReflectObject.hpp>\n"
       << "#include <cstdint>\n"
       << "#include <cstring>\n"
       << "#include <string>\n\n";

    // We need the actual type declarations — include the user headers
    // that were processed. For now emit a comment; the CMake integration
    // will add the proper include path / generated headers.
    os << "// User type headers are expected to be available via includes.\n"
       << "// Make sure -I paths or pre-includes are set correctly.\n\n";

    for (auto& ct : allTypes) {
      if (ct.isEnum) continue;       // enums get introspection, not create/destroy
      if (ct.isAbstract) continue;   // can't instantiate abstract types

      std::string safeName = sanitize_for_c(ct.qualifiedName);
      std::unordered_map<std::string, int> overloadCounters; // per-type, reset each iteration

      // ---- create / destroy ----
      os << fmt::format(
          "// ---- {} ----\n"
          "ZPC_REFLECT_PY_API void* zs_reflect_{}_create() {{\n"
          "  return static_cast<void*>(new {});\n"
          "}}\n\n",
          ct.qualifiedName, safeName, ct.nameNoTag);

      os << fmt::format(
          "ZPC_REFLECT_PY_API void zs_reflect_{}_destroy(void* p) {{\n"
          "  delete static_cast<{}*>(p);\n"
          "}}\n\n",
          safeName, ct.nameNoTag);

      // ---- type_name ----
      os << fmt::format(
          "ZPC_REFLECT_PY_API const char* zs_reflect_{}_type_name() {{\n"
          "  return R\"({})\";\n"
          "}}\n\n",
          safeName, ct.qualifiedName);

      // ---- field names ----
      {
        std::string joined;
        for (size_t i = 0; i < ct.fields.size(); ++i) {
          if (i > 0) joined += ",";
          joined += ct.fields[i].name;
        }
        os << fmt::format(
            "ZPC_REFLECT_PY_API const char* zs_reflect_{}_field_names() {{\n"
            "  return \"{}\";\n"
            "}}\n\n",
            safeName, joined);
      }

      // ---- method names ----
      {
        std::string joined;
        bool first = true;
        for (auto& m : ct.methods) {
          if (m.accessStr != "public") continue;
          if (m.isPureVirtual) continue;
          if (!first) joined += ",";
          joined += m.name;
          first = false;
        }
        os << fmt::format(
            "ZPC_REFLECT_PY_API const char* zs_reflect_{}_method_names() {{\n"
            "  return \"{}\";\n"
            "}}\n\n",
            safeName, joined);
      }

      // ---- per-field getter / setter ----
      for (auto& f : ct.fields) {
        if (f.isStatic) continue;
        std::string ctype = cpp_to_c_type(f.typeNameNoTag);
        bool isString = (ctype == "const char*");
        bool isSimple = !ctype.empty();

        if (isSimple) {
          // Getter
          if (isString) {
            // std::string → const char*  (copy via thread-local buffer)
            os << fmt::format(
                "ZPC_REFLECT_PY_API const char* zs_reflect_{}_get_{}(void* p) {{\n"
                "  return ::zs::reflect::py::returnString("
                "static_cast<{}*>(p)->{}.c_str());\n"
                "}}\n\n",
                safeName, f.name, ct.nameNoTag, f.name);
          } else {
            os << fmt::format(
                "ZPC_REFLECT_PY_API {} zs_reflect_{}_get_{}(void* p) {{\n"
                "  return static_cast<{}*>(p)->{};\n"
                "}}\n\n",
                ctype, safeName, f.name, ct.nameNoTag, f.name);
          }

          // Setter (if not const)
          if (!f.isConst) {
            if (isString) {
              os << fmt::format(
                  "ZPC_REFLECT_PY_API void zs_reflect_{}_set_{}(void* p, const char* v) {{\n"
                  "  static_cast<{}*>(p)->{} = v ? v : \"\";\n"
                  "}}\n\n",
                  safeName, f.name, ct.nameNoTag, f.name);
            } else {
              os << fmt::format(
                  "ZPC_REFLECT_PY_API void zs_reflect_{}_set_{}(void* p, {} v) {{\n"
                  "  static_cast<{}*>(p)->{} = v;\n"
                  "}}\n\n",
                  safeName, f.name, ctype, ct.nameNoTag, f.name);
            }
          }
        } else {
          // Complex type: expose as void* (pointer to field in-place)
          os << fmt::format(
              "ZPC_REFLECT_PY_API void* zs_reflect_{}_get_{}_ptr(void* p) {{\n"
              "  return static_cast<void*>(&static_cast<{}*>(p)->{});\n"
              "}}\n\n",
              safeName, f.name, ct.nameNoTag, f.name);
        }
      }

      // ---- per-method invoker ----
      for (auto& m : ct.methods) {
        if (m.accessStr != "public") continue;
        if (m.isPureVirtual) continue;
        if (m.isStatic) continue;

        // Check if ALL params and return type are simple C types
        std::string retCType = cpp_to_c_type(m.returnType);
        bool retIsString = (retCType == "const char*");
        bool allSimple = !retCType.empty() || m.returnType == "void";
        std::vector<std::string> paramCTypes;
        for (auto& p : m.params) {
          std::string pc = cpp_to_c_type(p.typeName);
          paramCTypes.push_back(pc);
          if (pc.empty()) allSimple = false;
        }

        if (!allSimple) {
          // Skip methods with complex parameter types for direct C-ABI
          // They can still be called via the Accessor/Any interface.
          continue;
        }

        bool isVoidReturn = (m.returnType == "void");
        std::string retType = isVoidReturn ? "void" : retCType;
        if (retIsString) retType = "const char*";

        // Build parameter signature
        std::string paramSig = "void* p";
        std::string argPass;
        for (size_t pi = 0; pi < m.params.size(); ++pi) {
          std::string pCType = paramCTypes[pi];
          bool pIsString = (pCType == "const char*");
          paramSig += fmt::format(", {} a{}", pCType, pi);
          if (pi > 0) argPass += ", ";
          if (pIsString) {
            // Convert const char* → std::string
            argPass += fmt::format("std::string(a{} ? a{} : \"\")", pi, pi);
          } else {
            argPass += fmt::format("a{}", pi);
          }
        }

        // Disambiguate overloaded methods by appending a sequential index
        // to the generated C symbol (avoids duplicate-symbol linker errors).
        std::string funcKey = safeName + "_" + m.name;
        int overloadIdx = overloadCounters[funcKey]++;
        std::string funcSuffix = overloadIdx > 0 ? fmt::format("_{}", overloadIdx) : "";

        os << fmt::format(
            "ZPC_REFLECT_PY_API {} zs_reflect_{}_call_{}{}({}) {{\n",
            retType, safeName, m.name, funcSuffix, paramSig);

        std::string callExpr;
        if (m.isConst) {
          callExpr = fmt::format(
              "static_cast<const {}*>(p)->{}({})",
              ct.nameNoTag, m.name, argPass);
        } else {
          callExpr = fmt::format(
              "static_cast<{}*>(p)->{}({})",
              ct.nameNoTag, m.name, argPass);
        }

        if (isVoidReturn) {
          os << fmt::format("  {};\n", callExpr);
        } else if (retIsString) {
          os << fmt::format("  return ::zs::reflect::py::returnString(({}).c_str());\n",
                            callExpr);
        } else {
          os << fmt::format("  return {};\n", callExpr);
        }
        os << "}\n\n";
      }

      // ---- generic accessor through Any (fallback) ----
      os << fmt::format(
          "ZPC_REFLECT_PY_API bool zs_reflect_{}_has_field(const char* name) {{\n"
          "  if constexpr (::zs::reflect::has_accessor_v<{}>) {{\n"
          "    return ::zs::reflect::AccessorOf<{}>::get().hasField(name);\n"
          "  }}\n"
          "  return false;\n"
          "}}\n\n",
          safeName, ct.nameNoTag, ct.nameNoTag);

      os << fmt::format(
          "ZPC_REFLECT_PY_API bool zs_reflect_{}_has_method(const char* name) {{\n"
          "  if constexpr (::zs::reflect::has_accessor_v<{}>) {{\n"
          "    return ::zs::reflect::AccessorOf<{}>::get().hasMethod(name);\n"
          "  }}\n"
          "  return false;\n"
          "}}\n\n",
          safeName, ct.nameNoTag, ct.nameNoTag);
    }

    // Ensure output directory exists
    auto parentDir = std::filesystem::path(outputPath).parent_path();
    if (!parentDir.empty()) mkdirs(parentDir.string());

    std::ofstream ofs(outputPath, std::ios::out | std::ios::trunc);
    if (!ofs) {
      std::cerr << fmt::format("Cannot write Python bindings: {}\n", outputPath);
      return;
    }
    ofs << os.str();
    if (verbose)
      fmt::print("  Wrote Python C-ABI bindings ({} types) → {}\n",
                  allTypes.size(), outputPath);
  }

  // -----------------------------------------------------------------------
  // emit_python_wrapper — Python ctypes wrapper module
  // -----------------------------------------------------------------------

  void emit_python_wrapper(const std::string& outputPath,
                           const std::vector<CollectedType>& allTypes,
                           const std::string& libName,
                           bool verbose) {
    std::ostringstream os;

    os << "# ============================================================\n"
       << "# Auto-generated by zpc_reflect_tool — DO NOT EDIT\n"
       << "# Python ctypes wrapper for: " << libName << "\n"
       << "# ============================================================\n"
       << "import ctypes\n"
       << "import os\n"
       << "import sys\n"
       << "from pathlib import Path\n\n"
       << "def _find_library(name: str):\n"
       << "    \"\"\"Find and load the shared library.\"\"\"\n"
       << "    search_paths = [\n"
       << "        Path(__file__).parent,\n"
       << "        Path(__file__).parent / 'lib',\n"
       << "        Path(__file__).parent / '..',\n"
       << "    ]\n"
       << "    if sys.platform == 'win32':\n"
       << "        suffixes = ['.dll', '.pyd']\n"
       << "    elif sys.platform == 'darwin':\n"
       << "        suffixes = ['.dylib', '.so']\n"
       << "    else:\n"
       << "        suffixes = ['.so']\n"
       << "    \n"
       << "    for d in search_paths:\n"
       << "        for sfx in suffixes:\n"
       << "            p = d / (name + sfx)\n"
       << "            if p.exists():\n"
       << "                return ctypes.cdll.LoadLibrary(str(p))\n"
       << "    # Last resort: let the OS search\n"
       << "    return ctypes.cdll.LoadLibrary(name)\n\n"
       << "_lib = _find_library('" << libName << "')\n\n";

    for (auto& ct : allTypes) {
      if (ct.isEnum) continue;
      if (ct.isAbstract) continue;

      std::string safeName = sanitize_for_c(ct.qualifiedName);
      std::string pyClassName = ct.displayName;
      // Make sure pyClassName is a valid Python identifier
      for (char& c : pyClassName) {
        if (!std::isalnum(c) && c != '_') c = '_';
      }

      os << "class " << pyClassName << ":\n"
         << "    \"\"\"Reflected type: " << ct.qualifiedName << "\"\"\"\n\n"
         << "    _create = _lib.zs_reflect_" << safeName << "_create\n"
         << "    _create.restype = ctypes.c_void_p\n"
         << "    _create.argtypes = []\n\n"
         << "    _destroy = _lib.zs_reflect_" << safeName << "_destroy\n"
         << "    _destroy.restype = None\n"
         << "    _destroy.argtypes = [ctypes.c_void_p]\n\n"
         << "    def __init__(self, _ptr=None):\n"
         << "        if _ptr is not None:\n"
         << "            self._ptr = _ptr\n"
         << "            self._owned = False\n"
         << "        else:\n"
         << "            self._ptr = self._create()\n"
         << "            self._owned = True\n\n"
         << "    def __del__(self):\n"
         << "        if self._owned and self._ptr:\n"
         << "            self._destroy(self._ptr)\n"
         << "            self._ptr = None\n\n";

      // Properties for each field
      for (auto& f : ct.fields) {
        if (f.isStatic) continue;
        std::string ctype = cpp_to_c_type(f.typeNameNoTag);
        bool isString = (ctype == "const char*");
        bool isSimple = !ctype.empty();

        if (isSimple) {
          std::string ctypesPy = c_type_to_ctypes(ctype);

          // getter
          os << "    @property\n"
             << "    def " << f.name << "(self):\n"
             << "        fn = _lib.zs_reflect_" << safeName << "_get_" << f.name << "\n"
             << "        fn.restype = " << ctypesPy << "\n"
             << "        fn.argtypes = [ctypes.c_void_p]\n";
          if (isString) {
            os << "        val = fn(self._ptr)\n"
               << "        return val.decode('utf-8') if val else ''\n\n";
          } else {
            os << "        return fn(self._ptr)\n\n";
          }

          // setter (if not const)
          if (!f.isConst) {
            os << "    @" << f.name << ".setter\n"
               << "    def " << f.name << "(self, value):\n"
               << "        fn = _lib.zs_reflect_" << safeName << "_set_" << f.name << "\n"
               << "        fn.restype = None\n"
               << "        fn.argtypes = [ctypes.c_void_p, " << ctypesPy << "]\n";
            if (isString) {
              os << "        fn(self._ptr, value.encode('utf-8') if isinstance(value, str) else value)\n\n";
            } else {
              os << "        fn(self._ptr, value)\n\n";
            }
          }
        } else {
          // Complex type: expose raw pointer
          os << "    @property\n"
             << "    def " << f.name << "_ptr(self):\n"
             << "        fn = _lib.zs_reflect_" << safeName << "_get_" << f.name << "_ptr\n"
             << "        fn.restype = ctypes.c_void_p\n"
             << "        fn.argtypes = [ctypes.c_void_p]\n"
             << "        return fn(self._ptr)\n\n";
        }
      }

      // Methods
      for (auto& m : ct.methods) {
        if (m.accessStr != "public") continue;
        if (m.isPureVirtual || m.isStatic) continue;

        std::string retCType = cpp_to_c_type(m.returnType);
        bool isVoidReturn = (m.returnType == "void");
        bool allSimple = !retCType.empty() || isVoidReturn;
        std::vector<std::string> paramCTypes;
        for (auto& p : m.params) {
          std::string pc = cpp_to_c_type(p.typeName);
          paramCTypes.push_back(pc);
          if (pc.empty()) allSimple = false;
        }
        if (!allSimple) continue; // skip methods with complex types

        std::string retCtypes;
        if (isVoidReturn) retCtypes = "None";
        else retCtypes = c_type_to_ctypes(retCType);

        bool retIsString = (retCType == "const char*");

        // Build Python method
        std::string pyParams = "self";
        std::string pyArgs = "self._ptr";
        for (size_t pi = 0; pi < m.params.size(); ++pi) {
          pyParams += fmt::format(", {}", m.params[pi].name.empty()
                                             ? fmt::format("arg{}", pi)
                                             : m.params[pi].name);
          std::string argName = m.params[pi].name.empty()
                                    ? fmt::format("arg{}", pi)
                                    : m.params[pi].name;
          bool pIsString = (paramCTypes[pi] == "const char*");
          if (pIsString) {
            pyArgs += fmt::format(", {}.encode('utf-8') if isinstance({}, str) else {}",
                                  argName, argName, argName);
          } else {
            pyArgs += fmt::format(", {}", argName);
          }
        }

        // argtypes
        std::string argtypesList = "ctypes.c_void_p";
        for (auto& pc : paramCTypes) {
          argtypesList += ", " + c_type_to_ctypes(pc);
        }

        os << "    def " << m.name << "(" << pyParams << "):\n"
           << "        fn = _lib.zs_reflect_" << safeName << "_call_" << m.name << "\n"
           << "        fn.restype = " << retCtypes << "\n"
           << "        fn.argtypes = [" << argtypesList << "]\n";
        if (isVoidReturn) {
          os << "        fn(" << pyArgs << ")\n\n";
        } else if (retIsString) {
          os << "        val = fn(" << pyArgs << ")\n"
             << "        return val.decode('utf-8') if val else ''\n\n";
        } else {
          os << "        return fn(" << pyArgs << ")\n\n";
        }
      }

      // Introspection helpers
      os << "    @staticmethod\n"
         << "    def _type_name():\n"
         << "        fn = _lib.zs_reflect_" << safeName << "_type_name\n"
         << "        fn.restype = ctypes.c_char_p\n"
         << "        fn.argtypes = []\n"
         << "        val = fn()\n"
         << "        return val.decode('utf-8') if val else ''\n\n"
         << "    @staticmethod\n"
         << "    def _field_names():\n"
         << "        fn = _lib.zs_reflect_" << safeName << "_field_names\n"
         << "        fn.restype = ctypes.c_char_p\n"
         << "        fn.argtypes = []\n"
         << "        val = fn()\n"
         << "        return (val.decode('utf-8') if val else '').split(',')\n\n"
         << "    @staticmethod\n"
         << "    def _method_names():\n"
         << "        fn = _lib.zs_reflect_" << safeName << "_method_names\n"
         << "        fn.restype = ctypes.c_char_p\n"
         << "        fn.argtypes = []\n"
         << "        val = fn()\n"
         << "        return (val.decode('utf-8') if val else '').split(',')\n\n"
         << "    def __repr__(self):\n"
         << "        return f'" << pyClassName << "({self._ptr:#x})'\n\n\n";
    }

    // Enum wrapper classes
    for (auto& ct : allTypes) {
      if (!ct.isEnum) continue;
      std::string pyClassName = ct.displayName;
      for (char& c : pyClassName) {
        if (!std::isalnum(c) && c != '_') c = '_';
      }

      os << "class " << pyClassName << ":\n"
         << "    \"\"\"Reflected enum: " << ct.qualifiedName << "\"\"\"\n";
      for (auto& ev : ct.enumValues) {
        os << "    " << ev.name << " = " << ev.value << "\n";
      }
      os << "\n\n";
    }

    // Ensure output directory exists
    auto parentDir = std::filesystem::path(outputPath).parent_path();
    if (!parentDir.empty()) mkdirs(parentDir.string());

    std::ofstream ofs(outputPath, std::ios::out | std::ios::trunc);
    if (!ofs) {
      std::cerr << fmt::format("Cannot write Python wrapper: {}\n", outputPath);
      return;
    }
    ofs << os.str();
    if (verbose)
      fmt::print("  Wrote Python wrapper → {}\n", outputPath);
  }

